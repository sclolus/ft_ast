#include "libft.h"

void	ft_putnbr_tab(uint32_t n, int32_t *tab)
{
	while (n--)
	{
		ft_putnbr(tab[n]);
	}
}

int main(void)
{
	int	*tab;

	tab = (int[]){12, 11, 13};
	ft_putnbr_tab(3, tab);
	return (0);
}
